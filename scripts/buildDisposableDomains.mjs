  'tutanota.com', 'tutamail.com', 'tuta.io',
  'hushmail.com', 'fastmail.com', 'fastmail.fm',
  // Major Chinese providers
  'qq.com', 'sohu.com', '163.com', '126.com', 'yeah.net',
  'sina.com', 'sina.cn', 'aliyun.com',
  // Major US / international providers
  'gmail.com', 'googlemail.com',
  'yahoo.com', 'yahoo.co.uk', 'yahoo.fr', 'yahoo.de', 'yahoo.it',
  'yahoo.es', 'yahoo.com.au', 'yahoo.ca', 'yahoo.com.br', 'ymail.com', 'rocketmail.com',
  'outlook.com', 'outlook.fr', 'outlook.de', 'outlook.it', 'outlook.es',
  'hotmail.com', 'hotmail.co.uk', 'hotmail.fr', 'hotmail.de', 'hotmail.it',
  'icloud.com', 'mac.com', 'me.com',
  'aol.com', 'aim.com',
  'gmx.com', 'gmx.net', 'gmx.de', 'gmx.us', 'gmx.co.uk', 'gmx.fr',
  'live.com', 'live.co.uk', 'live.fr', 'live.de', 'live.it',
  'msn.com',
  // Russian / CIS
  'mail.ru', 'list.ru', 'bk.ru', 'inbox.ru',
  'yandex.com', 'yandex.ru', 'ya.ru',
  // Other mainstream
  'zoho.com', 'zohomail.com',
