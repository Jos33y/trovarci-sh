#!/usr/bin/env node
/**
 * scripts/buildDisposableDomains.mjs
 *
 * Builds the canonical data/disposable_domains.txt from:
 *   - index.json (third-party aggregator list, ~120k entries)
 *   - data/disposable_domains.curated.txt (our additions / overrides, optional)
 *
 * Output is sorted, deduped, shape-validated, lowercased, with hardcoded
 * exclusions for legit services that aggregator lists sometimes mis-tag.
 *
 * Re-run anytime to refresh:
 *
 *     node scripts/buildDisposableDomains.mjs
 *
 * Optional flags:
 *
 *     --input <path>      Override index.json path
 *                         (default: ./index.json)
 *     --curated <path>    Override curated additions path
 *                         (default: ./data/disposable_domains.curated.txt)
 *     --output <path>     Override output path
 *                         (default: ./data/disposable_domains.txt)
 *     --no-curated        Skip the curated additions file even if present
 *
 * Exits non-zero on parse failure or if input is missing.
 *
 * The script is idempotent: running twice on the same inputs produces a
 * byte-identical output. That makes git diffs predictable.
 */

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { resolve } from 'node:path';

// ---------------------------------------------------------------------------
// CLI
// ---------------------------------------------------------------------------

const args = process.argv.slice(2);
function flag(name, fallback) {
  const i = args.indexOf(`--${name}`);
  if (i === -1) return fallback;
  return args[i + 1];
}
const HAS_NO_CURATED = args.includes('--no-curated');

const ROOT = process.cwd();
const INPUT_JSON   = resolve(ROOT, flag('input',   'index.json'));
const CURATED_TXT  = resolve(ROOT, flag('curated', 'data/disposable_domains.curated.txt'));
const OUTPUT_TXT   = resolve(ROOT, flag('output',  'data/disposable_domains.txt'));

// ---------------------------------------------------------------------------
// Header (regenerated every run, kept verbatim)
// ---------------------------------------------------------------------------

const HEADER = `# Disposable email provider domains.
#
# Bundled at build time, NOT fetched at runtime. Generated by
# scripts/buildDisposableDomains.mjs - do NOT edit this file directly,
# add domains to data/disposable_domains.curated.txt and re-run the
# build script.
#
# Lines starting with # and blank lines are ignored.
# All entries are lowercase. Comparison is exact (case-insensitive) on
# the email's domain part. Subdomains are NOT auto-matched - if a
# service uses subdomains for aliases, each one is listed explicitly.
#
# Source priority:
#   1. data/disposable_domains.curated.txt (this team's overrides)
#   2. index.json (third-party aggregator list)
#
# Excluded by policy (real services, NOT disposable):
#   - protonmail.com / proton.me / pm.me     (privacy mail)
#   - tutanota.com / tutamail.com / tuta.io  (privacy mail)
#   - qq.com / sohu.com / 163.com / 126.com  (mainstream Chinese)
#   - sina.com / sina.cn / aliyun.com
#   - hushmail.com / fastmail.com / fastmail.fm
#   - gmail.com / yahoo.com (and locales) / outlook.com / icloud.com
#   - aol.com / gmx.* / me.com / live.com / msn.com
#   - mail.ru / yandex.com / yandex.ru
#   - zoho.com / rediffmail.com / mail.com
#
# When a customer disputes a domain in this file, add it to the EXCLUDED
# set in scripts/buildDisposableDomains.mjs and re-run the build. Do NOT
# remove it from index.json - that file is treated as immutable input.
#
`;

// ---------------------------------------------------------------------------
// Hardcoded exclusions
//
// Legit services that aggregator lists occasionally tag as disposable.
// These are stripped regardless of what the source data says. Add to this
// set when a customer reports a false positive.
// ---------------------------------------------------------------------------

